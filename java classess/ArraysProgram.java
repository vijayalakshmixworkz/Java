public class ArraysProgram
{
	public static void main (String[] args)
	{
		int[] num={ 10,20,30,40};
		for( int i=0;i<num.length();i++)
		{
			System.out.println(""+num[i]);
		}
		
		// array reversal
		for( int j=num.length-10; j>=0;j--)
		{
			System.out.println("array elements in reverse order");
	
			System.out.println(""+num[i]);
		}
	}
}